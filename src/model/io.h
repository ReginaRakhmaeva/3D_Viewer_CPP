// IO.H - Классы для работы с файлами
//
// ЗАЧЕМ НУЖЕН:
// Обеспечивает загрузку 3D моделей из OBJ файлов. Реализует паттерн "Strategy"
// для различных форматов файлов (пока только OBJ). Обрабатывает ошибки
// и нормализует модели для корректного отображения.
//
// ЧТО СОДЕРЖИТ:
// - BaseFileReader абстрактный класс (интерфейс для чтения файлов) - паттерн Strategy
// - FileReader класс (конкретная реализация для OBJ файлов) - парсинг .obj
// - NormalizationParameters класс (параметры нормализации модели) - центрирование, масштабирование
// - FacadeOperationResult класс (результат операций с ошибками) - успех/ошибка + сообщение
// - Парсинг OBJ файлов: вершины (v x y z) и грани (f v1 v2 v3 ...)
// - Нормализация координат модели (центрирование в начало координат, масштабирование)
// - Обработка ошибок чтения файлов (файл не найден, неправильный формат, поврежденные данные)
//
// КАК РАБОТАЕТ:
// 1. FileReader::ReadScene() открывает OBJ файл
// 2. Парсит строки "v x y z" -> создает Vertex объекты
// 3. Парсит строки "f v1 v2 v3 ..." -> создает Edge объекты между вершинами
// 4. Создает Figure с вершинами и ребрами
// 5. Создает Scene с фигурой
// 6. Нормализует модель: центрирует в (0,0,0), масштабирует до нужного размера
// 7. Возвращает FacadeOperationResult с результатом (успех/ошибка)
//
// ПОДДЕРЖИВАЕМЫЕ ФОРМАТЫ:
// - OBJ файлы с вершинами (v) и гранями (f)
// - Планируется: PLY, STL (через новые стратегии FileReader)
//
// Все в namespace s21
