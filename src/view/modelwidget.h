// MODELWIDGET.H - Виджет для отображения 3D модели
//
// ЗАЧЕМ НУЖЕН:
// Обеспечивает отображение 3D модели в окне приложения. Использует простой
// подход без OpenGL - отрисовка через QPainter или простые Qt виджеты.
// Обрабатывает интерактивное управление моделью.
//
// ЧТО СОДЕРЖИТ:
// - ModelWidget класс (наследует от QWidget) - виджет для отображения модели
// - Методы для отрисовки 3D модели (paintEvent, updateModel)
// - Настройки отображения: цвет фона, цвет/толщина линий, цвет/размер вершин
// - Поддержка разных типов проекции (параллельная/центральная)
// - Методы для обновления данных модели (setModel, updateModel)
// - Обработка событий мыши для поворота/масштабирования/перемещения
// - Интеграция с SceneDrawerBase для отрисовки
// - Простая 3D проекция без сложных матриц
//
// КАК РАБОТАЕТ:
// 1. paintEvent(): отрисовка 3D модели через QPainter
// 2. updateModel(): обновление данных модели и перерисовка
// 3. mousePressEvent/mouseMoveEvent: обработка интерактивного управления
// 4. wheelEvent: масштабирование колесиком мыши
// 5. keyPressEvent: управление клавиатурой (поворот, сброс)
//
// ИНТЕРАКТИВНОЕ УПРАВЛЕНИЕ:
// - ЛКМ + перетаскивание: поворот модели
// - ПКМ + перетаскивание: перемещение модели
// - Колесико мыши: масштабирование
// - Клавиши: стрелки для поворота, +/- для масштабирования
//
// ПРИНЦИПЫ MVC:
// - НИКАКОЙ бизнес-логики, только отрисовка
// - Получает данные модели от Controller
// - Отображает модель без изменения данных
// - Интерактивные действия отправляет в Controller через сигналы
//
// Все в namespace s21
